package com.example.Cine.modelos;

public class Ingresos2 {
    private int mes;
    private int ingresos;

    public Ingresos2() {
    }

    public Ingresos2(int mes, int ingresos) {
        this.mes = mes;
        this.ingresos = ingresos;
    }

    public int getMes() {
        return mes;
    }

    public int getIngresos() {
        return ingresos;
    }

}
